# PART 2 — FARM CROP GROQ FALLBACK
# ============================================================

def groq_farm_predict(user_input: dict, transformer_guess: str = None, confidence: float = 0):
    context = ''
    if transformer_guess:
        context = f'A local ML model suggested "{transformer_guess}" with only {round(confidence*100,1)}% confidence, which is below the threshold. Please give a more reliable recommendation.'

    prompt = f'''You are an expert agricultural botanist.
Based on these soil and climate measurements, recommend the best crop to grow.

Measurements:
- Nitrogen (N): {user_input.get("N", 60)} kg/ha
- Phosphorus (P): {user_input.get("P", 40)} kg/ha
- Potassium (K): {user_input.get("K", 40)} kg/ha
- Temperature: {user_input.get("temperature", 25)}°C
- Humidity: {user_input.get("humidity", 60)}%
- Soil pH: {user_input.get("ph", 6.5)}
- Rainfall: {user_input.get("rainfall", 100)} mm

{context}

Choose from: rice, wheat, maize, chickpea, kidneybeans, pigeonpeas,
mothbeans, mungbean, blackgram, lentil, pomegranate, banana, mango, grapes,
watermelon, muskmelon, apple, orange, papaya, coconut, cotton, jute, coffee.

Respond ONLY in this exact JSON format:
{{
  "recommended_crop": "crop name",
  "confidence": 92,
  "reason": "2 sentence explanation based on the measurements",
  "care_tip": "one practical growing tip for this crop"
}}'''

    try:
        response = groq_client.chat.completions.create(
            model='llama-3.3-70b-versatile',
            messages=[{'role': 'user', 'content': prompt}],
            temperature=0.2,
            max_tokens=400
        )
        raw = response.choices[0].message.content.strip()
        if '```' in raw:
            raw = raw.split('```')[1].replace('json','').strip()
        return json.loads(raw)
    except Exception as e:
        return {
            'recommended_crop': transformer_guess or 'rice',
            'confidence'      : 65,
            'reason'          : 'Based on your soil and climate conditions.',
            'care_tip'        : 'Monitor soil moisture regularly.'
        }


