    """Diagnose from uploaded image using HuggingFace model + Groq"""
    try:
        # Handle both bytes and base64 string
        if isinstance(image_bytes_or_base64, str):
            img_bytes = base64.b64decode(
                image_bytes_or_base64.split(",")[1] if "," in image_bytes_or_base64
                else image_bytes_or_base64
            )
        else:
            img_bytes = image_bytes_or_base64

        headers = {"Authorization": f"Bearer {HF_TOKEN}"}
        res = requests.post(HF_API_URL, headers=headers, data=img_bytes, timeout=30)

        if res.status_code == 200:
            preds = res.json()
            if isinstance(preds, list) and preds:
                top        = preds[0]
                disease    = map_label(top.get("label", ""))
                confidence = round(top.get("score", 0.7) * 100, 1)
                # Pass image-detected disease as a symptom + extra context
                result = diagnose_with_groq(
                    [disease],
                    f"Image analysis detected: {disease} at {confidence}% confidence by MobileNetV2 model."
                )
                if result["success"]:
                    result["result"]["confidence"]  = confidence
                    result["result"]["detected_by"] = "MobileNetV2 + Groq AI"
                return result

        # HuggingFace failed — diagnose from visual symptoms generically
        return diagnose_with_groq(
            ["discoloration", "spots", "abnormal coloring"],
            "From uploaded image — HuggingFace model unavailable, diagnosing from visual symptoms."
        )

    except Exception as e:
        return diagnose_with_groq(
            ["general symptoms"],
            f"Image processing error: {str(e)}"
        )


