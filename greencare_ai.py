# PART 3 — HYBRID PIPELINE (with explainability)
# ============================================================

def hybrid_pipeline(user_input: dict) -> dict:
    """
    HYBRID PIPELINE
    ===============
    Step 1: TabTransformer predicts + generates explanation
    Step 2a: confidence >= 80% → return with full explanation
    Step 2b: confidence <  80% → escalate to Groq LLM
    """
    pipeline_stats['total'] += 1
    features = [
        user_input.get('N', 60),
        user_input.get('P', 40),
        user_input.get('K', 40),
        user_input.get('temperature', 25),
        user_input.get('humidity', 60),
        user_input.get('ph', 6.5),
        user_input.get('rainfall', 100)
    ]

    try:
        crop_name, confidence, top3, feat_importance, explanation = transformer_predict(features)
        print(f'🤖 Transformer → {crop_name} ({confidence*100:.1f}% confident)')

        if confidence >= CONFIDENCE_THRESHOLD:
            pipeline_stats['transformer_handled'] += 1
            print(f'✅ HIGH confidence — Transformer handles this (no API call)')
            return {
                'success'           : True,
                'source'            : 'FT-Transformer',
                'recommended_crop'  : crop_name,
                'confidence'        : round(confidence * 100, 1),
                'top3_predictions'  : top3,
                'explanation'       : explanation,
                'feature_importance': feat_importance,
                'reason'            : explanation['summary'],
                'why_recommended'   : explanation['why_recommended'],
                'care_tip'          : f'Monitor your {crop_name} crop weekly for best yield.',
                'pipeline_note'     : 'Handled by local FT-Transformer (no API cost)'
            }
        else:
            pipeline_stats['groq_handled'] += 1
            print(f'⚠️  LOW confidence ({confidence*100:.1f}%) — Escalating to Groq...')
            groq_result = groq_farm_predict(user_input, crop_name, confidence)
            print(f'🌐 Groq → {groq_result["recommended_crop"]} ({groq_result["confidence"]}% confident)')
            return {
                'success'              : True,
                'source'               : 'Groq API (Llama 3.3 70B)',
                'recommended_crop'     : groq_result['recommended_crop'],
                'confidence'           : groq_result['confidence'],
                'top3_predictions'     : top3,
                'explanation'          : explanation,
                'feature_importance'   : feat_importance,
                'reason'               : groq_result['reason'],
                'why_recommended'      : explanation['why_recommended'],
                'care_tip'             : groq_result['care_tip'],
                'transformer_guess'    : crop_name,
                'transformer_confidence': round(confidence * 100, 1),
                'pipeline_note'        : f'Escalated to Groq — transformer was {round(confidence*100,1)}% (below {CONFIDENCE_THRESHOLD*100}% threshold)'
            }

    except Exception as e:
        pipeline_stats['groq_handled'] += 1
        print(f'❌ Transformer error → Groq fallback: {e}')
        groq_result = groq_farm_predict(user_input)
        return {
            'success'          : True,
            'source'           : 'Groq API (Fallback)',
            'recommended_crop' : groq_result['recommended_crop'],
            'confidence'       : groq_result['confidence'],
            'reason'           : groq_result['reason'],
            'care_tip'         : groq_result['care_tip'],
            'pipeline_note'    : 'Groq fallback due to transformer error'
        }


